package JAVA_dsa_practice.Basics;

public class Relational_operators {
    public static void main(String[] args) {
        int p=10,q=15;
        System.out.println(p==q);//false
        System.out.println(p!=q);//true
        System.out.println(p<=q);//true
        System.out.println(p>=q);//false
        System.out.println(p<q);//ture
        System.out.println(p>q);//false
        
    }
}
