package JAVA_dsa_practice.Basics;


public class Increment_decrement {
    public static void main(String[] args) {
        //increment
        int x =5;
        System.out.println(x);
        x++;//post increment
        System.out.println(x);
        x--;//post decrement;
        System.out.println(x);
        x=7;
        ++x;//pre increment
        System.out.println(x);
        --x;//pre decrement;
        System.out.println(x);

    }
}
